def _copy_system_prompt(config_dir: Path) -> Path | None:
    """Copy system_prompt.md from package config to user config."""
    config_dir.mkdir(parents=True, exist_ok=True)
    dest = config_dir / "system_prompt.md"
    if dest.exists():
        return dest  # Don't overwrite user customizations

    source = Config.get_package_dir() / "config" / "system_prompt.md"
    if source.exists():
        shutil.copy2(source, dest)
        return dest
    return None


def _install_playwright_chromium() -> bool:
    """Install Playwright chromium browser if not present."""
    try:
        print("\n📦 Checking Playwright chromium...")
        result = subprocess.run(
            [sys.executable, "-m", "playwright", "install", "chromium"],
            capture_output=False,
            timeout=300,
        )
        if result.returncode == 0:
            print("✅ Playwright chromium ready")
            return True
        print("⚠️  Playwright install had issues (non-fatal)")
        return False
    except FileNotFoundError:
        print("⚠️  Playwright not found. Install manually: python -m playwright install chromium")
        return False
    except subprocess.TimeoutExpired:
        print("⚠️  Playwright install timed out (non-fatal)")
        return False
    except Exception as e:
        print(f"⚠️  Playwright install error: {e}")
        return False


def run_setup_wizard(force: bool = False) -> bool:
    """Run the interactive setup wizard.

    Args:
        force: If True, run even if already configured.

    Returns:
        True if setup completed successfully.
    """
    config_dir = get_user_config_dir()

    # Banner
    print("╔══════════════════════════════════════════════════╗")
    print("║       🚀 BPS Stat Agent — Setup Wizard          ║")
    print("╚══════════════════════════════════════════════════╝")
    print()

    # Load existing values as defaults
    existing = _read_existing_config(config_dir)

    if existing and not force:
        print("  ℹ️  Existing configuration found. Press Enter to keep current values.\n")

    # 1. AI Configuration
    print("━━━ AI Configuration ━━━")
    print("  Get your API key from your AI provider's dashboard.\n")

    ai_api_key = _prompt(
        "AI API Key",
        default=existing.get("ai_api_key", ""),
        required=True,
        masked=True,
    )

    ai_api_base = _prompt(
        "AI API Base URL",
        default=existing.get("ai_api_base", "") or "https://platform.minimax.io",
    )

    ai_model = _prompt(
        "AI Model",
        default=existing.get("ai_model", "") or "MiniMax-M2.5",
    )

    ai_provider = _prompt(
        "AI Provider (openai/anthropic)",
        default=existing.get("ai_provider", "") or "openai",
    )
    # Validate provider
    if ai_provider not in ("openai", "anthropic"):
        print(f"  ⚠️  Invalid provider '{ai_provider}', defaulting to 'openai'")
        ai_provider = "openai"

    # 2. BPS Configuration
    print("\n━━━ BPS Configuration ━━━")
    print("  Get your BPS API key from: https://webapi.bps.go.id")
    print("  (Free registration required)\n")

    bps_api_key = _prompt(
        "BPS API Key",
        default=existing.get("bps_api_key", ""),
        required=True,
    )

    # 3. Write config files
    print("\n━━━ Writing Configuration ━━━")

    config_path = _write_config_yaml(
        config_dir,
        ai_api_key=ai_api_key,
        ai_api_base=ai_api_base,
        ai_model=ai_model,
        ai_provider=ai_provider,
    )
    print(f"  ✅ {config_path}")

    mcp_path = _write_mcp_json(config_dir, bps_api_key=bps_api_key)
    print(f"  ✅ {mcp_path}")

    prompt_path = _copy_system_prompt(config_dir)
    if prompt_path:
        print(f"  ✅ {prompt_path}")

    # 4. Install Playwright
    _install_playwright_chromium()

    # 5. Summary
    print("\n╔══════════════════════════════════════════════════╗")
    print("║       ✅ Setup Complete!                         ║")
    print("╚══════════════════════════════════════════════════╝")
    print(f"\n  Config directory: {config_dir}")
    print(f"  AI Provider:     {ai_provider} ({ai_api_base})")
    print(f"  AI Model:        {ai_model}")
    print(f"  BPS API Key:     {'*' * 8}...{bps_api_key[-4:] if len(bps_api_key) > 4 else '****'}")
    print("\n  Run 'bpsagent' to start the agent.")
    print()

    return True
