import sys
from pathlib import Path

import yaml

from .config import Config


def get_user_config_dir() -> Path:
    """Return ~/.bps-stat-agent/config/."""
    return Path.home() / ".bps-stat-agent" / "config"


def needs_setup() -> bool:
    """Check if setup is needed (no config, invalid keys, or missing BPS key).

    Returns True if:
    - config.yaml not found
    - config.yaml has placeholder API key (in Config.INVALID_API_KEYS)
    - mcp.json not found or has empty BPS_API_KEY
    """
    try:
        config_path = Config.find_config_file("config.yaml")
        if not config_path:
            return True

        with open(config_path, encoding="utf-8") as f:
            data = yaml.safe_load(f)

        if not data:
            return True

        api_key = str(data.get("api_key", ""))
        if api_key in Config.INVALID_API_KEYS:
            return True

        # Check BPS API key in mcp.json
        mcp_path = Config.find_config_file("mcp.json")
        if not mcp_path:
            return True

        with open(mcp_path, encoding="utf-8") as f:
            mcp_data = json.load(f)

        bps_env = mcp_data.get("mcpServers", {}).get("bps", {}).get("env", {})
        bps_key = bps_env.get("BPS_API_KEY", "")
        if not bps_key:
            return True

        return False
    except Exception:
        return True


def _read_existing_config(config_dir: Path) -> dict:
    """Read existing config values as defaults for re-setup."""
    result = {}
    try:
        config_file = config_dir / "config.yaml"
        if config_file.exists():
            with open(config_file, encoding="utf-8") as f:
                data = yaml.safe_load(f) or {}
            api_key = str(data.get("api_key", ""))
            if api_key not in Config.INVALID_API_KEYS:
                result["ai_api_key"] = api_key
            result["ai_api_base"] = data.get("api_base", "")
            result["ai_model"] = data.get("model", "")
            result["ai_provider"] = data.get("provider", "")
    except Exception:
        pass

    try:
        mcp_file = config_dir / "mcp.json"
        if mcp_file.exists():
            with open(mcp_file, encoding="utf-8") as f:
                mcp_data = json.load(f)
            bps_key = mcp_data.get("mcpServers", {}).get("bps", {}).get("env", {}).get("BPS_API_KEY", "")
            if bps_key:
                result["bps_api_key"] = bps_key
    except Exception:
        pass

    return result


def _prompt(label: str, default: str = "", required: bool = False, masked: bool = False) -> str:
    """Interactive prompt with default value support."""
    while True:
        if masked:
            default_display = "****" if default else ""
        else:
            default_display = default

        if default_display:
            prompt_str = f"  {label} [{default_display}]: "
        else:
            prompt_str = f"  {label}: "

        try:
            if masked:
                value = getpass.getpass(prompt_str)
            else:
                value = input(prompt_str)
        except (EOFError, KeyboardInterrupt):
            print()
            return default

        value = value.strip()

        if not value and default:
            return default

        if not value and required:
            print("  ⚠️  This field is required. Please enter a value.")
            continue

        return value


